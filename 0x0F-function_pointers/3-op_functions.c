#include "3-calc.h"
#include <stdio.h>

/**
 * op_add - addition two numbers
 *
 * @a: integer
 * @b: integer
 *
 * Return: 0
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - difference two numbers
 *
 * @a: integer
 * @b: integer
 *
 * Return: 0
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply two numbers
 *
 * @a: integer
 * @b: integer
 *
 * Return: 0
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide two numbers
 *
 * @a: integer
 * @b: integer
 *
 * Return: 0
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - remainder of the division of two numbers
 *
 * @a: integer
 * @b: integer
 *
 * Return: 0
 */

int op_mod(int a, int b)
{
	return (a % b);
}
